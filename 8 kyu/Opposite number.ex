# -----------------------------------------------------------
# Very simple, given an integer or a floating-point number, find its opposite.
# 
# Examples:
# 
# 1: -1
# 14: -14
# -34: 34
# -----------------------------------------------------------

defmodule Opposite do
  def opposite(number) do
    0 - number
  end
end

