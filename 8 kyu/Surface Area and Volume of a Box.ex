# -----------------------------------------------------------
# Write a function that returns the total surface area and volume of a box as a list: {area, volume}
# -----------------------------------------------------------

defmodule Measurer do
  def get_size(w, h, d) do
    area = (w * h + w * d + h * d) * 2
    volume = w * h * d
    {area, volume}
  end
end

